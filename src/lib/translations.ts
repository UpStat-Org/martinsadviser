export function getTranslation(lang: Language, key: string): string {
  return translations[lang]?.[key] ?? translations.pt[key] ?? key;
}

export function getStoredLanguage(): Language {
  if (typeof localStorage === "undefined") return "pt";
  const stored = localStorage.getItem("app-language") as Language | null;
  return stored && ["pt", "en", "es"].includes(stored) ? stored : "pt";
}

export function tNow(key: string): string {
  return getTranslation(getStoredLanguage(), key);
}

